package operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        int number = 10;

        number += 10;
        System.out.println(number);

        number -= 10;
        System.out.println(number);

        number *= 2;
        System.out.println(number);

        number /= 2;
        System.out.println(number);

        number %= 3;
        System.out.println(number);
    }
}
